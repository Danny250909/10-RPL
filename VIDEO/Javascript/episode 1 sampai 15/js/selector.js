const hari = 2;
let hasil;

switch (hari) {
    case 1:
        hasil = "Senin";
        break;
    case 2:
        hasil = "Selasa";
        break;
    case 3:
        hasil = "Rabu";
        break;
    case 4:
        hasil = "Kamis";
        break;
    case 5:
        hasil = "Jumat";
        break;
    case 6:
        hasil = "Sabtu";
        break;
    case 7:
        hasil = "Minggu";
        break;
    default:
        hasil = "Hari tidak valid";
        break;
}

console.log(hasil);



